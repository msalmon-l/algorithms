#include "algorithms.h"

int	ft_check_num(char *num)
{
	int	i;
	
	if (!num)
		return (0);
	i = 0;
	while (num[i])
	{
		if (num[i] < 48 || num[i] > 57)
		{
			if (i == 0 && (num[i] != '-' && num[i] != '+'))
				return (-1);
		}
		i++;
	}
	return (i);
}
